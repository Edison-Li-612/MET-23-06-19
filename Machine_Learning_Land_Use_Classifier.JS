var sentinel2 = ee.ImageCollection('COPERNICUS/S2_SR_HARMONIZED')
                .filterDate('2020-01-01', '2020-01-30')
                .filterBounds(aoi)
                // Pre-filter to get less cloudy granules.
                .filter(ee.Filter.lt('CLOUDY_PIXEL_PERCENTAGE',20))
                .median();


var inputBands = ['B2','B3','B4','B8','B11'];

// Create a median composite to reduce the impact of clouds
var composite = sentinel2.select(inputBands).clip(aoi);

var visualization = {
min: 0,
max: 3000,
bands: ['B4','B3','B2']
};


Map.centerObject(aoi, 7);
Map.addLayer(composite, visualization, 'TrueColor');



var training = Water.merge(Urban).merge(Vegetation).merge(Barren_Cropfield_Village); 

var label = 'Class';

var input = composite.select(inputBands);

// Print input image to check available bands
print('Input Image:', input);

//Overlay the points on the imagery to get training
var overlay = input.sampleRegions({
  collection: training,
  properties:[label],
  scale:10
});

